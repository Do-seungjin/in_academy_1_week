package pkg01_arithmetic;

public class Practice02 {

	public static void main(String[] args) {
		int result = 100;
		result += 3 ;
		System.out.println(result);
		result -= 10 ;
		System.out.println(result);
		result *= 2 ;
		System.out.println(result);
		result /= 3 ;
		System.out.println(result);
		result %= 6 ;
		System.out.println(result);

	}

}
