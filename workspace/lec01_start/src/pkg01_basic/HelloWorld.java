package pkg01_basic;

public class HelloWorld {
	// 메인 메소드의 선언부
	public static void main(String[] args) {
		// 실행될 코드
		
		// 주석 단축키 컨트롤 슬래쉬
		/* 블록 주석 연습하기 주석 넣을때 마우스로 끌어서 컨트롤 쉬프트 슬래쉬 주석 취소할땐 컨트롤 쉬프트 역슬래쉬*/
		 
		System.out.print("Hello World~");
		System.out.print(123);
		// ln을 쓰면 자동 줄바꾸기 실행 줄바꾸기 있는 표준출력
		System.out.println("hello world");
		// escape 문자를 사용하여 더 많은 기능 사용 ex)하나의 텍스트 안에서 줄바꾸기->\n
		System.out.println(123);
		// 이스케이프 문자
		// "안녕하세요"로 출력을 원할 때
		System.out.println("안녕하세요");
		System.out.println("\"안녕하세요\"");
		// hello와 world 사이 줄바꾸기
		System.out.println("hello\nworld");
	}

}
